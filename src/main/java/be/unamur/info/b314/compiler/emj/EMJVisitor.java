    private String getTypeOfValue(EMJParser.RightexpContext rightCtx) {

        if (rightCtx.callfunc() != null) {
            // EMJParser.CallfuncContext ctx = rightCtx.callfunc();

            // Assuming callfunc returns the type of the function
            String functionName = rightCtx.callfunc().identif().getText();
            // String functionType = rightCtx.getParent()..
            String functionType = this.symbolTable.getFunctionReturnType(functionName);

            if (functionType == null) {
                return null;
            }
            ;
            return functionType;
        } else if (rightCtx.intexp() != null) {
            if (rightCtx.intexp().leftexp() != null) {
                return this.symbolTable.getVariableType(rightCtx.intexp().leftexp().identif().getText(), "");
            } else {
                return "🔢";
            }

        } else if (rightCtx.CHAR() != null) {
            return "🔣";
        } else if (rightCtx.STRING() != null) {
            return "🔡";
        } else if (rightCtx.tuple() != null) {
            if (!(rightCtx.tuple().STRING().isEmpty())) {
                return "👥(🔡)";
            } else if (!(rightCtx.tuple().CHAR().isEmpty())) {
                return "👥(🔣)";
            } else if (!(rightCtx.tuple().integer().isEmpty())) {
                return "👥(🔢)";
            } else if (!(rightCtx.tuple().booleanVal().isEmpty())) {
                return "👥(🔟)";
            }
        } else if (rightCtx.boolexp() != null) {
            // Handle boolean expressions
            return "🔟";
        }

        return ""; // Default case or error handling
    }

    private List<String> removeContentBetweenBracketsToList(String input) {
        List<String> elements = new ArrayList<>();
        StringBuilder currentElement = new StringBuilder();
        boolean insideBracket = false;

        for (char c : input.toCharArray()) {
            if (c == '[') {
                insideBracket = true;
                if (currentElement.length() > 0) {
                    elements.add(currentElement.toString());
                    currentElement.setLength(0); // clear the StringBuilder
                }
            } else if (c == ']') {
                insideBracket = false;
            } else if (!insideBracket && c != ',') {
                currentElement.append(c);
            }
        }

        if (currentElement.length() > 0) {
            elements.add(currentElement.toString());
        }

        return elements;
    }

    private List<String> getContentBetweenBrackets(String input) {
        List<String> elements = new ArrayList<>();
        StringBuilder currentElement = new StringBuilder();
        boolean insideBracket = false;

        for (char c : input.toCharArray()) {
            if (c == '[') {
                insideBracket = true;
                currentElement.setLength(0); // clear the StringBuilder
            } else if (c == ']') {
                insideBracket = false;
                elements.add(currentElement.toString());
            } else if (insideBracket) {
                currentElement.append(c);
            }
        }

        return elements;
    }

    private String getTypeOfIntexp(EMJParser.IntexpContext ctx) {
        // Déterminez le type de l'expression (entier, identificateur, etc.) et renvoyez
        // le type correspondant
        if (ctx.integer() != null) {
            return "🔢"; // Type entier
        } else if (ctx.leftexp() != null) {
            // Supposons que vous ayez une méthode pour obtenir le type de l'identificateur
            
            return getTypeOfLeftexp(ctx.leftexp());
        } else if (!ctx.intexp().isEmpty()) {
            // Si la sous-expression est un intexp, traitons chaque sous-expression
            // individuellement
            String firstType = null;
            for (EMJParser.IntexpContext subExp : ctx.intexp()) {
                String subExpType = getTypeOfIntexp(subExp);
                if (firstType == null) {
                    firstType = subExpType;
                } else if (!firstType.equals(subExpType)) {
                    // Gérez l'erreur ici selon vos besoins
                }
            }
            // Retournez le type de la première sous-expression (ou de toute autre logique
            // de gestion d'erreur)
            return firstType != null ? firstType : "inconnu";
        }

        // Autres cas à traiter si nécessaire
        return "inconnu"; // Valeur par défaut
    }

    private String getTypeOfLeftexp(EMJParser.LeftexpContext ctx) {
        String identif = ctx.identif().getText();
        String type;

        if (ctx.IDGETTER() != null) {
            type = getContentInsideParentheses(this.symbolTable.getVariableType(identif, ""));

        } else {
            type = this.symbolTable.getVariableType(identif, "");
        }

        return type;
    }

    private String getContentInsideParentheses(String input) {
        int startIndex = input.indexOf('(');
        int endIndex = input.lastIndexOf(')');
        if (startIndex != -1 && endIndex != -1 && endIndex > startIndex) {
            return input.substring(startIndex + 1, endIndex);
        }
        return ""; // Si les parenthèses sont mal formées ou absentes
    }

    public Object visitBoolexp(EMJParser.BoolexpContext ctx) {
        if (!ctx.comparator().isEmpty()) {
            // Si c'est un comparateur, les deux sous-expressions doivent être des intexp
            if (ctx.boolexp().size() == 2) {
                EMJParser.BoolexpContext leftExp = ctx.boolexp(0);
                EMJParser.BoolexpContext rightExp = ctx.boolexp(1);

                // Obtenez les types des deux sous-expressions
                String leftType = getTypeOfBoolexp(leftExp);
                String rightType = getTypeOfBoolexp(rightExp);

                // Comparez les types
                if (!leftType.equals(rightType)) {
                    // Erreur : les types des expressions gauche et droite sont différents
                    this.errorLogger.addError(new EMJError("invalidLogic", ctx.getText(), ctx.start.getLine()));
                }
            }
        } else if (!ctx.connector().isEmpty()) {
            // Si c'est un connecteur, les deux sous-expressions doivent être des boolean
            if (ctx.boolexp().size() == 2) {
                EMJParser.BoolexpContext leftExp = ctx.boolexp(0);
                EMJParser.BoolexpContext rightExp = ctx.boolexp(1);
                String type = this.symbolTable.getVariableType(leftExp.getText(), "");

                // Obtenez les types des deux sous-expressions
                String leftType = getTypeOfBoolexp(leftExp);
                String rightType = getTypeOfBoolexp(rightExp);

                if (type != null) {
                    if (!type.equals("🔟")) {
                        this.errorLogger.addError(new EMJError("invalidLogic", ctx.getText(), ctx.start.getLine()));
                    }

                }

                // Comparez les types
                if (!leftType.equals("🔟") || !rightType.equals("🔟")) {
                    // Erreur : les types des sous-expressions doivent être des boolean pour un
                    // connecteur
                    this.errorLogger.addError(new EMJError("invalidLogic", ctx.getText(), ctx.start.getLine()));
                }
            }
        }
        return super.visitBoolexp(ctx);
    }

    // Méthode pour obtenir le type d'une sous-expression booléenne
    private String getTypeOfBoolexp(EMJParser.BoolexpContext ctx) {
        // Déterminez le type de la sous-expression et renvoyez le type correspondant
        if (ctx.intexp() != null) {
            return "🔟"; // Type boolean pour une expression arithmétique

        } else if (ctx.booleanVal() != null) {
            return "🔟"; // Type boolean
        } else if (!ctx.boolexp().isEmpty()) {

            // Si la sous-expression est un boolexp, traitons chaque sous-expression
            // individuellement
            String firstType = null;
            for (EMJParser.BoolexpContext subExp : ctx.boolexp()) {
                String subExpType = getTypeOfBoolexp(subExp);
                if (firstType == null) {
                    firstType = subExpType;
                } else if (!firstType.equals(subExpType)) {
                    // Si les types des sous-expressions sont différents, il y a une erreur
                    return "🔟"; // Type boolean
                }
            }
            // Retournez le type de la première sous-expression
            return firstType != null ? firstType : "🔟"; // Ou une autre logique de gestion d'erreur
        } else if (!ctx.comparator().isEmpty()) {
            return "🔟"; // Type boolean pour une expression de comparaison
        }

        return "ERROR"; // Si aucun cas ne correspond
    }

