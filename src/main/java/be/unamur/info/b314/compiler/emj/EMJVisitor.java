    @Override
    public Object visitProgramme(EMJParser.ProgrammeContext ctx) {

        int nb_function = ctx.funcdecl().size();

        printer.setNumberOfFunctions(nb_function);

        // Parcourir les déclarations de fonction et enregistrer dans la SymbolTable
        for (EMJParser.FuncdeclContext funcDecl : ctx.funcdecl()) {
            String functionName = funcDecl.identif().getText();
            String returnType = funcDecl.functype().getText();
            String scope = funcDecl.getParent().getText();
            String params;
            List<String> funcParamsTypes;
            List<String> funcParamsIds;

            if (funcDecl.funcparams().funcparamsnotempty() != null) {
                params = funcDecl.funcparams().funcparamsnotempty().getText();
                funcParamsTypes = removeContentBetweenBracketsToList(params);
                funcParamsIds = getContentBetweenBrackets(params);

            } else {
                funcParamsTypes = new ArrayList<>();
                funcParamsIds = new ArrayList<>();
            }


            String returnedType;

            String varId;
            String scope2;
            String type;
            String value = null;

            for (int i = 0; i < funcDecl.innerFunction().vardecl().size(); i++) {

                varId = funcDecl.innerFunction().vardecl(i).identif().getText();
                scope2 = funcDecl.getText();
                type = funcDecl.innerFunction().vardecl(i).type().getText();

                if (funcDecl.innerFunction().vardecl(i).assignation() != null) {
                    value = funcDecl.innerFunction().vardecl(i).assignation().rightexp().getText();

                }

                if (!this.symbolTable.variableExists(varId, scope2)) {
                    // this.errorLogger.addError( new EMJError("functionIdentifierDoesNotExist",
                    // ctx.identif().getText(), ctx.start.getLine()));
                    this.symbolTable.registerVariable(varId, type, scope2, value);

                } else {

                    this.errorLogger.addError(new EMJError("varIdAlreadyExists", ctx.getText(), ctx.start.getLine()));

                }

            }

            if (funcDecl.returnInstruction().funcreturn().rightexp() == null) {
                returnedType = "🌀";

            } else {
                returnedType = getTypeOfValue(funcDecl.returnInstruction().funcreturn().rightexp());

            }

            if (!this.symbolTable.functionExists(functionName)) {
                if (returnType.equals(returnedType)) {
                    this.symbolTable.registerFunction(functionName, returnType, funcParamsTypes, funcParamsIds);
                } else {
                    this.errorLogger
                            .addError(new EMJError("functInvalidReturnType", ctx.getText(), ctx.start.getLine()));
                }
            } else {
                this.errorLogger.addError(new EMJError("idFunctAlreadyExists", ctx.getText(), ctx.start.getLine()));
            }

        }

        return super.visitProgramme(ctx);
    }

    @Override
    public Object visitIntexp(EMJParser.IntexpContext ctx) {

        if (ctx.operator().size() > 0) {

            for (int i = 0; i < ctx.operator().size(); i++) {

                EMJParser.OperatorContext operator = ctx.operator(i);
                if (operator.getText().equals("/")) {

                    EMJParser.IntexpContext denominator = ctx.intexp(i + 1);

                    if (denominator.leftexp() != null) {

                        String varName = denominator.leftexp().identif().getText();
                        String varValue = this.symbolTable.getVariableValue(varName);

                        if (varValue != null && varValue.equals("0")) {
                            this.errorLogger.addError(
                                    new EMJError("CanNotDivideByVarWithValue0 ", ctx.getText(), ctx.start.getLine()));
                        }

                    }
                }
            }

            // Vérifiez s'il y a un opérateur et deux éléments dans l'expression
            if (ctx.intexp().size() == 2) {
                // Obtenez les deux éléments de l'expression
                EMJParser.IntexpContext leftExp = ctx.intexp(0);
                EMJParser.IntexpContext rightExp = ctx.intexp(1);

                // Obtenez les types des deux éléments
                String leftType = getTypeOfIntexp(leftExp);
                String rightType = getTypeOfIntexp(rightExp);

                if (ctx.intexp(0).leftexp() != null && ctx.intexp(0).leftexp().IDGETTER() != null) {

                    String varId = ctx.intexp(0).leftexp().identif().getText();
                    String type = getContentInsideParentheses(this.symbolTable.getVariableType(varId, ""));

                    leftType = type;

                }

                if (ctx.intexp(1).leftexp() != null && ctx.intexp(1).leftexp().IDGETTER() != null) {

                    String varId = ctx.intexp(1).leftexp().identif().getText();
                    String type = getContentInsideParentheses(this.symbolTable.getVariableType(varId, ""));

                    rightType = type;
                }


                // Comparez les types
                if (!leftType.equals(rightType)) {
                    this.errorLogger.addError(new EMJError("invalidLogic", ctx.getText(), ctx.start.getLine()));
                }
            }
        }

        return super.visitIntexp(ctx);
    }

